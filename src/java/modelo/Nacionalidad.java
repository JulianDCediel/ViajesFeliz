package modelo;

public class Nacionalidad {

    int Id;
    String nom;

    public Nacionalidad() {
    }

    public Nacionalidad(int Id, String nom) {
        this.Id = Id;
        this.nom = nom;
    }

    public int getId() {
        return Id;
    }

    public void setId(int Id) {
        this.Id = Id;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

  

}
